// ==================== LANGUAGE SWITCHER LOGIC ====================
(function() {
  'use strict';

  // Detect language: URL param > localStorage > browser preference > 'en'
  var LANG = (function() {
    var urlParam = new URLSearchParams(location.search).get('lang');
    if (urlParam && I18N[urlParam]) return urlParam;
    var stored = localStorage.getItem('calc_site_lang');
    if (stored && I18N[stored]) return stored;
    // Match browser language
    var nav = (navigator.language || 'en').split('-')[0];
    var map = { zh: 'zh', es: 'es', de: 'de', fr: 'fr' };
    if (map[nav]) return map[nav];
    return 'en';
  })();
  window.LANG = LANG;

  // Translation function
  window.t = function(key, fallback) {
    return (I18N[LANG] && I18N[LANG][key]) || (I18N['en'] && I18N['en'][key]) || fallback || key;
  };

  // Set lang on <html>
  document.documentElement.lang = LANG === 'zh' ? 'zh-CN' : LANG === 'es' ? 'es' : LANG === 'de' ? 'de' : LANG === 'fr' ? 'fr' : 'en';

  // Apply translations to page on load
  window.applyI18n = function() {
    // Apply all [data-i18n] elements
    var els = document.querySelectorAll('[data-i18n]');
    for (var i = 0; i < els.length; i++) {
      var key = els[i].getAttribute('data-i18n');
      var translated = window.t(key);
      if (translated) {
        // If the element has an input/textarea, set value; otherwise set textContent
        if (els[i].tagName === 'INPUT' || els[i].tagName === 'TEXTAREA') {
          els[i].setAttribute('placeholder', translated);
        } else if (els[i].tagName === 'META' && els[i].getAttribute('name') === 'description') {
          els[i].setAttribute('content', translated);
        } else {
          els[i].textContent = translated;
        }
      }
    }
    // Apply [data-i18n-html] for HTML content
    var htmlEls = document.querySelectorAll('[data-i18n-html]');
    for (var j = 0; j < htmlEls.length; j++) {
      var htmlKey = htmlEls[j].getAttribute('data-i18n-html');
      var htmlTranslated = window.t(htmlKey);
      if (htmlTranslated) htmlEls[j].innerHTML = htmlTranslated;
    }
  };

  // Switch language
  window.switchLang = function(lang) {
    LANG = lang;
    document.documentElement.lang = lang === 'zh' ? 'zh-CN' : lang === 'es' ? 'es' : lang === 'de' ? 'de' : lang === 'fr' ? 'fr' : 'en';
    localStorage.setItem('calc_site_lang', lang);
    window.applyI18n();
    // Refresh page to re-run any language-dependent logic (charts, calculators, etc.)
    // For data-heavy pages, we do a soft refresh
    var sel = document.getElementById('langSelect');
    if (sel) sel.value = lang;
  };

})();
